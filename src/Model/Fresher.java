package Model;

/**
 *
 * @author thaip
 */
public class Fresher extends Candidate{
    private int graduationDate;
    private String graduationRank, education;

    public Fresher() {
    }

    public Fresher(int graduationDate, String graduationRank, String education) {
        this.graduationDate = graduationDate;
        this.graduationRank = graduationRank;
        this.education = education;
    }

    public Fresher(int graduationDate, String graduationRank, String education, String candidateID, String firstName, String lastName, String adddress, String email, int candidateType, int birthDate, String phoneNumber) {
        super(candidateID, firstName, lastName, adddress, email, candidateType, birthDate, phoneNumber);
        this.graduationDate = graduationDate;
        this.graduationRank = graduationRank;
        this.education = education;
    }

    public int getGraduationDate() {
        return graduationDate;
    }

    public void setGraduationDate(int graduationDate) {
        this.graduationDate = graduationDate;
    }

    public String getGraduationRank() {
        return graduationRank;
    }

    public void setGraduationRank(String graduationRank) {
        this.graduationRank = graduationRank;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    @Override
    public String toString() {
        return super.toString();
    }
    
}
